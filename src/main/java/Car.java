public class Car {

    private String color;
    private String motor;
    private int door;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMotor() {
        return motor;
    }

    public void setMotor(String motor) {
        this.motor = motor;
    }

    public int getDoor() {
        return door;
    }

    public int setDoor(int door) {
        this.door = door;
        return door;
    }
}
